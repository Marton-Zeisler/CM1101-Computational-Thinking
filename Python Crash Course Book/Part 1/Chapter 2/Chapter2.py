# Strings
message = "Hello world"
print(message)

print("marci zeisler".title())
print("lowers".upper())

firstName = "Peter"
lastName = "Park"
print(firstName + " \n\t" + lastName)

userName = "   user12  "
print(userName.rstrip().lstrip() + "!") # removes whitespaces from right and left
print(userName.strip() + "!") # strips left and right together

